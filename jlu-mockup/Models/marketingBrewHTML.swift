<p style="line-height:22px;margin-top:0;margin-bottom:15px"><span>Just like letting your significant other know your iPhone passcode, data privacy was up for debate a few days ago. Jokes aside, </span><a href="https://adage.com/article/digital/why-digital-advertisers-are-concerned-about-californias-latest-privacy-law/2292081?utm_source=ad-age-cmo-strategy&amp;utm_medium=email&amp;utm_campaign=20201104&amp;utm_content=article4-headline&amp;utm_source=morning_brew" target="_blank" class="link" rel="noopener" style="border-bottom:2px solid #1c7ff2;text-decoration:none;color:#333">Proposition 24</a><span> is one of the surest election outcomes we've gotten all week.</span></p>
<p style="line-height:22px;margin-top:0;margin-bottom:15px">On Wednesday, California voted in favor of Prop 24, also known as the California Privacy Rights and Enforcement Act (CPRA), which will be certified on December 11.</p>
<ul style="margin-top:0;padding-left:30px">
<li style="line-height:22px;margin-bottom:10px">Prop 24 generally gives consumers more protection over certain types of data, including their ethnicity, geographic location, health, religion, and sexual orientation, <a href="https://www.adweek.com/programmatic/what-media-industry-needs-to-know-californias-privacy-law-cpra/?itm_source=parsely-api&amp;utm_source=morning_brew" target="_blank" class="link" rel="noopener" style="border-bottom:2px solid #1c7ff2;text-decoration:none;color:#333">among others</a>. </li>
<li style="line-height:22px;margin-bottom:10px">It also makes it so consumers under the age of 16 must opt in to have their data sold, and will triple the fine for violators.</li>
</ul>
<p style="line-height:22px;margin-top:0;margin-bottom:15px">Plus, California will now give a squad of regulators up to $10 million a year to enforce privacy regulations like the California Consumer Privacy Act (CCPA), the state's comprehensive privacy bill passed in 2018.</p>
<p style="line-height:22px;margin-top:0;margin-bottom:15px"><strong style="color:#000">Looking ahead:</strong> It might be too soon to fully comprehend Prop 24’s impact, but for now, it’s important to think about “how cross-contextual behavioral advertising will work in this new world, repercussions of consumers opting out, and penalties that brands and marketers will have to face if they are noncompliant,” MediaLink’s VP of Data and Tech Solutions Reshma Karnik told Adweek.</p>

